// SSH hardening module.
//
// Owner profile: solo dev who uses Claude Code remote (outbound HTTPS relay),
// NOT plain SSH from phone. So the safest default is to *disable* Remote Login
// entirely. If SSH must stay on (e.g. occasional local SSH for tmux/mosh),
// apply a hardening config drop-in.
//
// Hardening drop-in lives at /etc/ssh/sshd_config.d/99-mac-bootstrap.conf so
// macOS upgrades of /etc/ssh/sshd_config don't clobber it. We validate via
// `sshd -t` before reload — a misconfigured sshd is a hard lockout.

import path from "node:path";

export const name = "ssh-hardening";

const DROP_IN_PATH = "/etc/ssh/sshd_config.d/99-mac-bootstrap.conf";

export const HARDENING_CONFIG = `# mac-bootstrap SSH hardening drop-in.
# Generated by ~/projects/mac-bootstrap/src/security/ssh-hardening.js.
# Owner: solo dev. Threat model: opportunistic network attacks.
PasswordAuthentication no
ChallengeResponseAuthentication no
KbdInteractiveAuthentication no
PubkeyAuthentication yes
PermitRootLogin no
PermitEmptyPasswords no
MaxAuthTries 3
LoginGraceTime 30
ClientAliveInterval 300
ClientAliveCountMax 2
X11Forwarding no
AllowAgentForwarding no
AllowTcpForwarding no
PrintMotd no
UseDNS no
`;

async function isRemoteLoginEnabled(runner) {
  const result = await runner.run("systemsetup", ["-getremotelogin"]);
  if (result.exitCode !== 0) return null;
  return /On\b/i.test(result.stdout || "");
}

async function readDropIn(runner) {
  const result = await runner.run("/bin/cat", [DROP_IN_PATH]);
  if (result.exitCode !== 0) return { exists: false, content: null };
  return { exists: true, content: result.stdout || "" };
}

export async function detect({ runner } = {}) {
  if (!runner) throw new Error("ssh-hardening.detect: runner is required");
  const remoteLogin = await isRemoteLoginEnabled(runner);
  const dropIn = await readDropIn(runner);
  return {
    ok: true,
    remoteLoginOn: remoteLogin,
    dropInExists: dropIn.exists,
    dropInMatches: dropIn.content === HARDENING_CONFIG,
    detail: { dropInPath: DROP_IN_PATH },
  };
}

export async function suggest({ current } = {}) {
  if (!current) return { advice: "run detect first" };
  if (current.remoteLoginOn === false) {
    return { advice: "Remote Login is OFF — no SSH attack surface. No action needed." };
  }
  if (current.remoteLoginOn === null) {
    return { advice: "could not determine Remote Login state (systemsetup requires sudo on some configs)" };
  }
  // Remote Login is ON
  if (current.dropInExists && current.dropInMatches) {
    return { advice: "Remote Login ON; hardening drop-in present and matches. OK." };
  }
  return {
    advice: "Remote Login is ON. If you don't use SSH, disable it. Otherwise apply the hardening drop-in.",
    options: [
      { label: "PREFERRED: disable Remote Login", command: "sudo systemsetup -setremotelogin off" },
      {
        label: "ALTERNATIVE: keep SSH but harden it",
        commands: [
          `sudo tee ${DROP_IN_PATH} > /dev/null <<'EOF'\n${HARDENING_CONFIG}EOF`,
          "sudo sshd -t   # validate — must exit 0",
          "sudo launchctl kickstart -k system/com.openssh.sshd",
        ],
      },
    ],
  };
}

export async function apply({ runner, logger = console, dryRun = false, mode = "harden" } = {}) {
  if (!runner) throw new Error("ssh-hardening.apply: runner is required");
  const current = await detect({ runner });
  if (mode === "disable") {
    if (current.remoteLoginOn === false) {
      logger.log("ssh-hardening: Remote Login already off — noop");
      return { changed: false };
    }
    if (dryRun) {
      logger.log("ssh-hardening: DRY RUN — would run `sudo systemsetup -setremotelogin off`");
      return { changed: false, dryRun: true };
    }
    const r = await runner.run("sudo", ["systemsetup", "-setremotelogin", "off"]);
    return r.exitCode === 0 ? { changed: true, action: "disabled" } : { changed: false, error: r.stderr };
  }
  // mode = "harden"
  if (current.remoteLoginOn === false) {
    logger.log("ssh-hardening: Remote Login is off — no hardening needed (no attack surface)");
    return { changed: false };
  }
  if (current.dropInExists && current.dropInMatches) {
    logger.log("ssh-hardening: drop-in already in place and matches — noop");
    return { changed: false };
  }
  if (dryRun) {
    logger.log(`ssh-hardening: DRY RUN — would write ${DROP_IN_PATH}, validate with sshd -t, kickstart sshd`);
    return { changed: false, dryRun: true };
  }
  // Write drop-in via sudo tee
  const writeCmd = `sudo tee ${DROP_IN_PATH} > /dev/null`;
  logger.log(`ssh-hardening: writing ${DROP_IN_PATH}`);
  const writeResult = await runner.run("/bin/sh", ["-c", `${writeCmd} <<'__EOF__'\n${HARDENING_CONFIG}__EOF__`]);
  if (writeResult.exitCode !== 0) return { changed: false, error: `write failed: ${writeResult.stderr}` };
  // Validate
  logger.log("ssh-hardening: validating with sshd -t");
  const validate = await runner.run("sudo", ["sshd", "-t"]);
  if (validate.exitCode !== 0) {
    logger.log("ssh-hardening: VALIDATION FAILED — removing drop-in to prevent lockout");
    await runner.run("sudo", ["/bin/rm", "-f", DROP_IN_PATH]);
    return { changed: false, error: `sshd -t failed: ${validate.stderr}` };
  }
  // Reload sshd
  logger.log("ssh-hardening: reloading sshd via launchctl kickstart");
  const reload = await runner.run("sudo", ["launchctl", "kickstart", "-k", "system/com.openssh.sshd"]);
  return reload.exitCode === 0
    ? { changed: true, action: "hardened" }
    : { changed: true, action: "hardened-needs-manual-reload", note: "drop-in written + validated, but sshd reload failed; reboot or run `sudo launchctl kickstart -k system/com.openssh.sshd`" };
}

export { DROP_IN_PATH };
